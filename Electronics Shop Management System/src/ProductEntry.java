    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String a=bprice.getText();
        String b=sprice.getText();

        int sum=Integer.parseInt(a);
        int sum1=Integer.parseInt(b);

        if(sum <=0){
            JOptionPane.showMessageDialog(this, "You cannot have 0 Buying Price ");
            return;

        }else if(sum1<=0){
            JOptionPane.showMessageDialog(this, "Cannot have 0 selling Price  ");
            return;
        }else if(sum >= sum1){
            JOptionPane.showMessageDialog(this, "Sellng Price cannot be lower than Buying price");
            return;
        }

        if(name.getText().equals("")||bprice.getText().equals("")||sprice.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please fill all the fields to proceed");
            return;

        }

        try{
            String sql="select * from Product where Name=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1, name.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "The product name already exist");
                return;
            }
            pst.close();
            rs.close();

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        try{
            String sql="insert into Product(Category,Name,Buy_Price,Sell_price,Quantity) Values(?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1,(String)jComboBox1.getSelectedItem());
            pst.setString(2, name.getText());
            pst.setString(3, bprice.getText());
            pst.setString(4, sprice.getText());
            pst.setString(5, quantity.getText());
            pst.execute();
            JOptionPane.showMessageDialog(this, "Product Successfuly Saved");
            pst.close();
            rs.close();
               table1();
            name.setText("");
            bprice.setText("");
            sprice.setText("");
            quantity.setText("");

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

