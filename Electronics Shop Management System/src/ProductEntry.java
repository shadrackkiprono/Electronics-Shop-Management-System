    public void update(){
    
    
         if(name.getText().equals("")||bprice.getText().equals("")||sprice.getText().equals("")){

            JOptionPane.showMessageDialog(this, "Please select the product to update");
            return;

        }
        String a=bprice.getText();
        String b=sprice.getText();
        
        int sum=Integer.parseInt(a);
        int sum1=Integer.parseInt(b);
   
        if(sum <=0){
            JOptionPane.showMessageDialog(this, "You cannot have 0 Buying Price ");
            return;

        }else if(sum1<=0){
            JOptionPane.showMessageDialog(this, "Cannot have 0 selling Price  ");
            return;
        }else if(sum >= sum1){
            JOptionPane.showMessageDialog(this, "Sellng Price cannot be lower than Buying price");
            return;
        }
        try{
            int p=JOptionPane.showConfirmDialog(null, "Are you sure wanna update","Update",JOptionPane.YES_NO_OPTION);
            if(p==0){
                String value=name.getText();
                String value1=bprice.getText();
                String value2=sprice.getText();

                String sql="update Product set Buy_Price='"+value1+"',Sell_Price='"+value2+"' where Name='"+value+"'  ";
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Successfully updated");
                pst.close();
                rs.close();
            
              
            }
        }catch(Exception e){
            
        }
    }
