    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(category.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter product category");
            return;
        }
        try{
            String sql="select * from Category where Cat_Name=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1, category.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Product Category Already Exist");
                return;
            }
            pst.close();
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        try{
            String sql="insert into Category(Cat_Name) Values(?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, category.getText());
            pst.execute();
            JOptionPane.showMessageDialog(this, "Product Category Sucessfully Saved");
          

            category.setText("");
            pst.close();
            rs.close();
             table();
     
      

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

