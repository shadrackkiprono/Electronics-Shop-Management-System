    private void searchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode()==KeyEvent.VK_ENTER){
               if(search.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Empty fields");
        return;
        }
        try{
            String sql="select * from Category where Cat_Name=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1,search.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                String add=rs.getString("Cat_Name");
                category.setText(add);
            
            }else{
                JOptionPane.showMessageDialog(this, "Category name does not exist in databse");
                return;
            
            }
            pst.close();
            rs.close();
       
        }catch(Exception e){
        JOptionPane.showMessageDialog(this, e);
        }
        
      
         
         }
       
    }//GEN-LAST:event_searchKeyPressed

